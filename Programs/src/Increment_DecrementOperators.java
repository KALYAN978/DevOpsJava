
public class Increment_DecrementOperators {

	public static void main(String[] args) {
		
		int result = 1;
		result = result + 1;
		System.out.println(result);
		
		int result1 = 2;
		result1++;
		System.out.println(result1);
		
		int check = 2;
		check--;
		System.out.println(check);
		
		
		int a = 1;
		a -= 1;
		System.out.println(a);
		
		
		int b = 10;
		b = (int) (b - 5.5);
		System.out.println(b);

	}

}
