package Switch;

public class Switch1 {

	public static void main(String[] args) {
		
		char charValue = 'A';
		switch(charValue) {
		case 'A':
			System.out.println("A is able");
			break;
		case 'B':
			System.out.println("B is tom");
			break;
		case 'C':
			System.out.println("C is Cuhs");
			break;
		default:
			System.out.println("Default");
			break;
		}

	}

}
